story = "once upon a time there was a man name addy whose original name was Mehul Batra"

# Strings functions
print(len(story)) #len counts the number of character in a sentence i.e 78
print(story.endswith("Batra"))
print(story.count("a"))  #this will count number of a in a sentnce
print(story.capitalize())  #You can see it capitalises the first "O" of the sentence
print(story.find("addy"))#This will tell that "addy" is present at 38 number
print(story.find("Ram krishan")) #This will bring "-1" which means that there is no Ram krishan
print(story.replace("Mehul", "Ram Krishan"))






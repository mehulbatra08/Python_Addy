l1 = [1,8,7,2,21,15]
l1.sort() #sort the list
l1.reverse() #reverses the list
print(l1)
l1.append(69) #appends at the end of the list(adds at the end of the list)
print(l1)
l1.append(87)
print(l1)
l1.insert(0,544) #this means that 544 is added to 0th value i.e 1
print(l1)
l1.insert(2,544) #This means that 544 is added to 2nd value that is 7
print(l1)
l1.pop(2) #this will remove element number 2nd i.e 7 from l1
print(l1)
l1.remove(21)  #removes 21 from the list
print(l1)
